package smarthome.drivers;

public class GarageDoors {

    public void up(){
        System.out.println("Garage doors was move up");
    }

    public void down(){
        System.out.println("Garage doors was move down");
    }

    public void stop(){
        System.out.println("Garage doors was stopped");
    }

    public void lightOn(){
        System.out.println("Garage doors light was turned on");
    }

    public void lightOff(){
        System.out.println("Garage doors light was turned off");
    }
}
